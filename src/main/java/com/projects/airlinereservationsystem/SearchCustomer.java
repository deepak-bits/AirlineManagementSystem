    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        String CustID = customerId.getText();
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/ReservationSystem","root","");
            pre = con.prepareStatement("Select * from Customer where CustID = ?");
            pre.setString(1, CustID);

            ResultSet rs = pre.executeQuery();
            rs.next();
            
            firstName.setText(rs.getString("FirstName"));
            lastName.setText(rs.getString("LastName"));
            passport.setText(rs.getString("PassportID"));
            nic.setText(rs.getString("NicID"));
            address.setText(rs.getString("Address"));
            String Gender = rs.getString("Gender");
            contact.setText(rs.getString("Contact"));
                     
            if(Gender.equals("Male")) {
                male.setSelected(true);
                female.setSelected(false);
            } else {
                female.setSelected(true);
                male.setSelected(false);
            }
            
            try {
                Date date = new SimpleDateFormat("yyyy-MM-dd").parse(rs.getString("DoB"));
                dob.setDate(date);
            } catch (ParseException ex) {
                Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
            
     
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchBtnActionPerformed

