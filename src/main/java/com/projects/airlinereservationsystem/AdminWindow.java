    private void AutoID() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/ReservationSystem","root","");
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery("Select MAX(UserID) from Admin");
            rs.next();
            if(rs.getString("MAX(UserID)")==null) {
                userId.setText("US001");
            } else {
                long id = Long.valueOf(rs.getString("MAX(UserID)").substring(2));
                id++;
                userId.setText("US"+String.format("%03d",id));
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

